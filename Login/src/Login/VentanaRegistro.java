    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
       
        
        try{
            
            ps = (PreparedStatement) conectar.prepareStatement("INSERT INTO usuarios (nombre_completo,correo_electronico,nombre_usuario,contrasena) VALUES (?,?,?,?)");
            ps.setString(1, txtNcomp.getText());
            ps.setString(2, txtCorreo.getText());
            ps.setString(3, txtNusuario.getText());
            ps.setString(4, txtPass.getText());
            
            
            
            int res = ps.executeUpdate();
            if (res > 0){
                JOptionPane.showMessageDialog(null, "Registro Correcto");
                limpiarCajas();
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Error en Registro, Intente de Nuevo!");
                limpiarCajas();
                dispose();
            }
            conectar.close();
        } catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error :"+e);
            
        }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

